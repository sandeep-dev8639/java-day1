public class Dtypes {
    public static void main(String[] args) {
        byte b=120;
        short s=159;
        int i=12000000;
        float f=10.99999f;
        double d=10.33223d;
        char ch = 'a';
        System.out.println(b);
        System.out.println(s);
        System.out.println(i);
        System.out.println(f);
        System.out.println(d);
        System.out.println(ch);
    }
}
